// long (long int) - also 4 bytes?
// float - 4 bytes - 10^-38 to 10^38. 7 digits
// double - 8 bytes - 10^-308 to 10^308. 18 digits
// long double - 10 bytes - 10^-4932 to 10^4932 - 19 digits
// char - 1 byte - represents a character. ASCII!
//
// ^^ size of each primitive varies by compiler 
// integers are used in fixed point arithmetic
// floats are used for numbers with decimals (or floating points)
//
// REAL division and integer division
